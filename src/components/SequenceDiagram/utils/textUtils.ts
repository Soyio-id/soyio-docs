import { DIAGRAM_CONSTANTS } from '../constants';

export const wrapText = (text: string): string[] => {
  const words = text.split(' ');
  const lines: string[] = [];
  let currentLine = '';

  words.forEach(word => {
    const testLine = currentLine ? `${currentLine} ${word}` : word;
    if (testLine.length <= DIAGRAM_CONSTANTS.TEXT.MAX_LINE_WIDTH) {
      currentLine = testLine;
    } else {
      lines.push(currentLine);
      currentLine = word;
    }
  });

  if (currentLine) {
    lines.push(currentLine);
  }

  return lines;
};

export const getTextWidth = (text: string): number => text.length * 8;
